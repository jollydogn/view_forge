namespace ViewForge.Enums;

/// <summary>
/// Defines the sort direction for dynamic sorting.
/// </summary>
public enum SortDirection
{
    /// <summary>Sort in ascending order (A-Z, 0-9, oldest first).</summary>
    Ascending = 1,

    /// <summary>Sort in descending order (Z-A, 9-0, newest first).</summary>
    Descending = 2
}
